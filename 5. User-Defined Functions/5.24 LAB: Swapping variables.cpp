void SwapValues (int& userVal1, int& userVal2) 
{
   int swap; 
   swap = userVal1;
   userVal1 = userVal2; 
   userVal2 = swap;
}

int main() {
   /* Type your code here. Your code must call the function.  */
   
   int userVal1, userVal2;
   cin >> userVal1 >> userVal2;
   SwapValues (userVal1, userVal2);
   cout << userVal1 <<" "<< userVal2 << endl;

   return 0;
}
